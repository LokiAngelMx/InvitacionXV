// countdown.js
const countdown = () => {
    const countDate = new Date('Jul 6, 2024 00:00:00').getTime();
    const now = new Date().getTime();
    const gap = countDate - now;

    const second = 1000;
    const minute = second * 60;
    const hour = minute * 60;
    const day = hour * 24;

    const textDay = Math.floor(gap / day);
    const textHour = Math.floor((gap % day) / hour);
    const textMinute = Math.floor((gap % hour) / minute);
    const textSecond = Math.floor((gap % minute) / second);

    document.querySelector('#countdown .p-2:nth-child(1)').innerText = `${textDay} \n d√≠as`;
    document.querySelector('#countdown .p-2:nth-child(2)').innerText = `${textHour} \n horas`;
    document.querySelector('#countdown .p-2:nth-child(3)').innerText = `${textMinute} \n minutos`;
    document.querySelector('#countdown .p-2:nth-child(4)').innerText = `${textSecond} \n segundos`;
};

setInterval(countdown, 1000);